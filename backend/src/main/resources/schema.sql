CREATE TABLE PROJETO (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL
);

CREATE TABLE TAREFA (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITULO VARCHAR(100) NOT NULL,
    DESCRICAO VARCHAR(500),
    STATUS VARCHAR(30),
    DATA_CRIACAO TIMESTAMP DEFAULT CURRENT TIMESTAMP,
    ID_PROJETO BIGINT,
    CONSTRAINT FK_TAREFA_PROJETO FOREIGN KEY (ID_PROJETO) REFERENCES PROJETO(ID)
);

CREATE INDEX IDX_TAREFA_PROJETO_DATA ON TAREFA (ID_PROJETO, DATA_CRIACAO);